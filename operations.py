def add(num_1, num_2):
    result = num_1 + num_2
    print(f'{num_1} + {num_2} is equal to {result}')
    return result


def minus(num_1, num_2):
    result = num_1 - num_2
    print(f'{num_1} - {num_2} is equal to {result}')
    return result


def mult(num_1, num_2):
    result = num_1 * num_2
    print(f'{num_1} * {num_2} is equal to {result}')
    return result


def div(num_1, num_2):
    result = num_1 / num_2
    print(f'{num_1} / {num_2} is equal to {result}')
    return result


def power(num_1, num_2):
    result = num_1 ** num_2
    print(f'{num_1} ** {num_2} is equal to {result}')
    return result


def module(num_1, num_2):
    result = num_1 % num_2
    print(f'{num_1} % {num_2} is equal to {result}')
    return result