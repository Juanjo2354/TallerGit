# Función suma
def add(num_1, num_2):
    result = num_1 + num_2
    print(f'{num_1} + {num_2} is equal to {result}')
    return result


# Función resta
def minus(num_1, num_2):
    result = num_1 - num_2
    print(f'{num_1} - {num_2} is equal to {result}')
    return result


# Función multiplicación
def mult(num_1, num_2):
    result = num_1 * num_2
    print(f'{num_1} * {num_2} is equal to {result}')
    return result


# Función división
def div(num_1, num_2):
    result = num_1 / num_2
    print(f'{num_1} / {num_2} is equal to {result}')
    return result


# Función potencia
def power(num_1, num_2):
    result = num_1 ** num_2
    print(f'{num_1} ** {num_2} is equal to {result}')
    return result


# Función módulo
def module(num_1, num_2):
    result = num_1 % num_2
    print(f'{num_1} % {num_2} is equal to {result}')
    return result


